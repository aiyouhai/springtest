package com.tt.ui.pojo;

public class SysYg {
	private long id;
	private String ygName;
	private long deptId;

	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}

	public String getYgName() {
		return ygName;
	}

	public void setYgName(String ygName) {
		this.ygName = ygName;
	}

	public long getDeptId() {
		return deptId;
	}

	public void setDeptId(long deptId) {
		this.deptId = deptId;
	}

}
